    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource().equals(search_btn)) {
            search_btn.setBackground(Color.black);
            search_btn.setForeground(Color.white);
            Employee tempEmployee = new Employee();

            tempEmployee = find(Integer.parseInt(id.getText()));

            id_show.setText(tempEmployee.getId() + "");
            name_show.setText(tempEmployee.getName());
            address_show.setText(tempEmployee.getAddress());
            rank_show.setText(tempEmployee.getRank());
            hours_worked_show.setText(tempEmployee.getHours_worked() + "");
            overtime_show.setText(tempEmployee.getOvertime_worked() + "");
            total_salary_show.setText(tempEmployee.getTotal_salary() + "");

            search_btn.setBackground(Color.white);
            search_btn.setForeground(Color.black);
        }
    }
