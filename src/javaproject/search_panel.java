 public Employee find(int id) {//gets the file with employee's data and store the data in Employee obj then return it
        int tempId;
        String tempName = "", tempAddress = "", tempRank = "";
        int tempHours_worked, tempOvertime;
        double tempTotal_salary = 0;
        try {
            Scanner myFile = new Scanner(new File(id + ".txt"));

            tempId = myFile.nextInt();
            tempName = myFile.next();
            tempAddress = myFile.next();
            tempRank = myFile.next();
            tempHours_worked = myFile.nextInt();
            tempOvertime = myFile.nextInt();

            if (myFile.hasNext()) {
                tempTotal_salary = Double.parseDouble(myFile.next());
            } else {
                JOptionPane.showMessageDialog(this, "Employee doesn't have salary", "Error", JOptionPane.ERROR_MESSAGE);
            }

            Employee tempEmployee = new Employee(tempId, tempName, tempAddress, tempRank, tempHours_worked, tempHours_worked);
            tempEmployee.setTotal_salary(tempTotal_salary);
            return tempEmployee;
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Employee Not Found", "Error", JOptionPane.ERROR_MESSAGE);
        }

        Employee tempEmployee = new Employee();
        return tempEmployee;

    }

